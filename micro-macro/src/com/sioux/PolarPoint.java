package com.sioux;

public class PolarPoint {

    private double r;
    private double theta;

    PolarPoint(double r, double theta){

    }

    public double getR() {
        return r;
    }

    public void setR(double r) {
        this.r = r;
    }

    public double getTheta() {
        return theta;
    }

    public void setTheta(double theta) {
        this.theta = theta;
    }
}
